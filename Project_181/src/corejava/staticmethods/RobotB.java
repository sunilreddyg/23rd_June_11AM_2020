package corejava.staticmethods;


public class RobotB 
{

	
	static void walk()
	{
		System.out.println("walk executed");
	}
	
	
	static void run()
	{
		System.out.println("run executed");
	}
	

	public static void main(String[] args) 
	{
		
		RobotB.walk();
		RobotB.run();
	}

}
